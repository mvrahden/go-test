type regexpW struct {
	*regexp2.Regexp
}

func (r *regexpW) MatchString(s string) bool {
	res, _ := r.Regexp.MatchString(s)
	return res
}

var (
	GEN_TESTSUITE_FILE   = regexp.MustCompile(`^// Code generated by "gotest \(github\.com/mvrahden/go-test\)"; DO NOT EDIT\.(?:$|\n)`)
	IS_TEST_SUITE        = &regexpW{regexp2.MustCompile(`^(?!ƒƒ_GOTEST_|_)(?:X_|F_)?.+TestSuite$`, regexp2.ECMAScript)}
	IS_TEST_SUITE_METHOD = &regexpW{regexp2.MustCompile(`^(?:BeforeAll|AfterAll|BeforeEach|AfterEach|SuiteConfig|SuiteGuard|(?:X_|F_)?Test.+)$`, regexp2.ECMAScript)}
	IS_BEFORE_ALL        = regexp.MustCompile(`^BeforeAll$`)
	IS_AFTER_ALL         = regexp.MustCompile(`^AfterAll$`)
	IS_BEFORE_EACH       = regexp.MustCompile(`^BeforeEach$`)
	IS_AFTER_EACH        = regexp.MustCompile(`^AfterEach$`)
	IS_TEST_CASE       = &regexpW{regexp2.MustCompile(`^(?:X_|F_)?Test.+$`, regexp2.ECMAScript)} // matches all test cases
	IS_TEST_CASE_ASYNC = regexp.MustCompile(`^(?:X_|F_)?Test.+Async$`)                          // matches all test cases with async suffix
)

type TestSuiteSpecSet []*TestSuiteSpec
type SkippedTestSuites []*TestSuiteSpec
type SkippedTestCases map[*TestSuiteSpec][]*TestSuiteMethod

func (ts TestSuiteSpecSet) ReduceToEffectiveSet() (TestSuiteSpecSet, SkippedTestSuites, SkippedTestCases) {
	effectiveSet := append((TestSuiteSpecSet)(nil), ts...)

	var skippedTestSuites SkippedTestSuites
	skippedTestCases := SkippedTestCases{}
	{ // split focused from unfocused
		focused, unfocused := slices.SplitBy(effectiveSet, func(v *TestSuiteSpec, _ int) bool {
			return v.IsFocused()
		})
		effectiveSet = unfocused
		if len(focused) > 0 {
			effectiveSet, skippedTestSuites = focused, append(skippedTestSuites, unfocused...)
		}
	}

	{ // split excluded from included
		excluded, included := slices.SplitBy(effectiveSet, func(v *TestSuiteSpec, _ int) bool {
			return v.IsExcluded()
		})
		effectiveSet = included
		if len(excluded) > 0 {
			skippedTestSuites = append(skippedTestSuites, excluded...)
		}
	}

	slices.Range(effectiveSet, func(v *TestSuiteSpec, _ int) {
		skippedTestCases[v] = nil
	})

	{ // reduce SEQUENTIAL test cases
		slices.Range(effectiveSet, func(v *TestSuiteSpec, _ int) {
			// split focused from unfocused
			focused, unfocused := slices.SplitBy(v.th.TestCases, func(v *TestSuiteMethod, _ int) bool {
				return v.IsFocused()
			})
			v.th.TestCases = unfocused
			if len(focused) > 0 {
				v.th.TestCases = focused
				skippedTestCases[v] = append(skippedTestCases[v], unfocused...)
			}

			// split excluded from included
			excluded, included := slices.SplitBy(v.th.TestCases, func(v *TestSuiteMethod, _ int) bool {
				return v.IsExcluded()
			})
			v.th.TestCases = included
			if len(excluded) > 0 {
				skippedTestCases[v] = append(skippedTestCases[v], excluded...)
			}
		})
	}

	return effectiveSet, skippedTestSuites, skippedTestCases
}

type TestSuiteSpec struct {
	pkg                *packages.Package
	n                  ast.Node
	ts                 *ast.TypeSpec
	typ                *types.Struct
	underlyingTypeName string // non-empty for aliases of generic types (e.g. "GenericTestSuite")
	th                 *TestSuiteHarness
	fixture            *FixtureSpec
	fixtureFieldName   string // field name used in the suite struct to hold the fixture pointer
}

// Identifier returns the Types Name.
//
// FOR RENDERING
func (ts *TestSuiteSpec) PackageName() string {
	return ts.pkg.Name
}

// Identifier returns the Types Name.
//
// FOR RENDERING
func (ts *TestSuiteSpec) Identifier() string {
	return ts.ts.Name.Name
}

// StructType returns the underlying *types.Struct for field inspection.
func (ts *TestSuiteSpec) StructType() *types.Struct { return ts.typ }

// Fixture returns the fixture spec embedded in this test suite, or nil.
//
// FOR RENDERING
func (ts *TestSuiteSpec) Fixture() *FixtureSpec { return ts.fixture }

// SetFixture sets the fixture spec for this test suite.
func (ts *TestSuiteSpec) SetFixture(f *FixtureSpec) { ts.fixture = f }

// FixtureFieldName returns the field name used in the suite struct for the fixture pointer.
func (ts *TestSuiteSpec) FixtureFieldName() string { return ts.fixtureFieldName }

// SetFixtureFieldName sets the field name used in the suite struct for the fixture pointer.
func (ts *TestSuiteSpec) SetFixtureFieldName(name string) { ts.fixtureFieldName = name }

// FullIdentifier returns the Types Name plus it's package identifier with dot-notation.
//
// FOR RENDERING
func (ts *TestSuiteSpec) FullIdentifier() string {
	if ts.IsPxTestSuite() {
		return ts.Identifier()
	}
	return ts.pkg.Name + "." + ts.Identifier()
}

// IsPxTestSuite gives info about the test suites declarated package.
//
// FOR RENDERING
func (ts *TestSuiteSpec) IsPxTestSuite() bool {
	return strings.HasSuffix(ts.pkg.Name, "_test")
}

